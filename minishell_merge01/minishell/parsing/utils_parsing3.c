#include "../../../includes/parsing.h"

int    manage_num(char *s, int i)
{
    int j;
    
    j = i;
    while (s[i] >= '0' && s[i] <= '9')
        i++;
    if (s[i] == '>' || s[i] == '<')
        return (i);
    else
        return (j);
}